SELECT   corp.FACILITY_ID,
         corp.CORPORATION_ID, corp.NAME, 
         c.CUSTOMER_NBR_STND, c.NAME, 
         c.STATUS_CD,
         shd.INVOICE_NBR,
         shd.BILLING_DATE,
         shd.SPECIAL_COST_CDE_TYPE, shd.ITEM_NBR_HS, shd.ITEM_DESCRIPTION, 
         sum((shd.QTY_SOLD - shd.QTY_SCRATCHED) * shd.FINAL_SELL_AMT) sales
FROM     CRMADMIN.T_WHSE_CORPORATION corp 
         inner join CRMADMIN.T_WHSE_CUST c on corp.FACILITY_ID = c.FACILITYID and corp.CORPORATION_ID = c.CUST_CORPORATION 
         inner join CRMADMIN.T_WHSE_SALES_HISTORY_DTL shd on c.FACILITYID = shd.FACILITYID and c.CUSTOMER_NO_FULL = shd.CUSTOMER_NO_FULL
WHERE    corp.CORPORATION_ID in (879, 548, 726)
AND      shd.BILLING_DATE = '2020-02-10'
AND      shd.SPECIAL_COST_CDE_TYPE in ('F', 'G')
AND      shd.ITEM_NBR_HS in ('0030650', '0030668', '0030692')
GROUP BY corp.FACILITY_ID, corp.CORPORATION_ID, corp.NAME, c.CUSTOMER_NBR_STND, c.NAME, 
         c.STATUS_CD, shd.INVOICE_NBR, shd.BILLING_DATE, 
         shd.SPECIAL_COST_CDE_TYPE, shd.ITEM_NBR_HS, shd.ITEM_DESCRIPTION
;


SELECT   shd.TRANSACTION_DATE,
         shd.FACILITY_ID,
         corp.CORPORATION_ID,
         corp.CORPORATION_NAME,
         shd.CUSTOMER_NBR,
         c.CUSTOMER_NAME,
         shd.INVOICE_NBR,
         shd.ITEM_NBR,
         i.ROOT_ITEM_DESC,
         sum(shd.TOTAL_SALES_AMT) tot_sales
FROM     WH_OWNER.DC_SALES_HST shd 
         inner join WH_OWNER.DC_ITEM i on i.FACILITY_ID = shd.FACILITY_ID and i.ITEM_NBR = shd.ITEM_NBR 
         inner join WH_OWNER.DC_CUSTOMER c on shd.FACILITY_ID = c.FACILITY_ID and shd.CUSTOMER_NBR = c.CUSTOMER_NBR 
         inner join WH_OWNER.DC_CORPORATION corp on c.CORPORATION_ID = corp.CORPORATION_ID
WHERE    shd.FACILITY_ID = 58
AND      shd.TRANSACTION_DATE = '2020-02-10'
AND      shd.INVOICE_NBR in (2374802, 2374803, 2374804, 2374851, 2374852, 2374853, 2374854, 2374855, 2374857, 2374970, 2374971, 2374972, 2375000, 2375001, 2375003, 2375004, 2375005, 2375006, 2375010, 2375013, 2375014, 2375015, 2375023, 2375024)
AND      i.ITEM_NBR in (30650, 30668, 30692)
GROUP BY shd.TRANSACTION_DATE, shd.FACILITY_ID, corp.CORPORATION_ID, 
         corp.CORPORATION_NAME, shd.CUSTOMER_NBR, c.CUSTOMER_NAME, 
         shd.ITEM_NBR, shd.INVOICE_NBR, i.ROOT_ITEM_DESC
;